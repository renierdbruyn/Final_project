         <h1>How to be a great mentor</h1>Although mentoring an employee requires a fair deal of effort, the rewards are well worth it. Before taking the plunge read these tips to make sure you’re at the top of your mentor .
<br><br><img src="<?php echo base_url(); ?>assets/img/99.jpg" class="img-rounded" height="100px" width="200px"/><br>

<h1>Assign</h1>
Mentors must learn to assign tasks and be clear and concise about what is needed. The employee you’re mentoring needs structure and goals to grow, that’s where you as a mentor come in: provide support where needed and set up regular reviews to monitor their progress.
<h1>Encourage</h1>
Positive reinforcement is the best way to get spirits up. If your employee does well in an assigned task, congratulate them on a task well done. This will encourage your employee to keep doing well and more importantly, be consistently well in every task you delegate to them. Whether it’s a simple pat on the back or a lunch outing, find a way to acknowledge their achievement.  
<h1>Give constructive feedback</h1>
Try to regularly provide your employee with feedback. Make notes as they do their job and arrange regular sessions to catch up. Make sure your feedback is constructive as well. If it’s negative, break the news in a professional manner and provide tips on how they can improve. Your employee needs to know what they are doing right or wrong if they are to succeed.
<h1>Set up goals</h1>
In your regular catch up sessions, set up goals together. Discuss what a personal development plan and advise what is needed in order to reach the goals. This will help your employee focus on specific goals and gain a better understanding of what is needed.


